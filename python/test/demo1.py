# 10进制,2进制,8进制,16进制
# 进制转换
print(0b10) #2进制表示0b
print(0o11) #8进制表示0o
print(0x1F) #16进制表示0x

print(bin(10)) #10进制转化为2进制  bin向2进制转化
print(int(0b111)) #2进制转化为10进制  int向10进制转化
print(oct(0b111))#2进制转化为8进制  oct向8进制转化
print(hex(888)) #10进制转化为16进制  hex向16进制转化


